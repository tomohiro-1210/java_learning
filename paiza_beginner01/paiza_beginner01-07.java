//計算する
public class Main {
	public static void main(String[] args) {
		int number = 100 + 20;
		int number1 = 300;
		int number2 = 40;
		System.out.println(number * number1 / 30);
		System.out.println(number / number + number1 / number);
		System.out.println(number1 % number);
		System.out.println(number + number1 / number2 * number1 + number % number2);
	}
}

